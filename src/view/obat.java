    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        // TODO add your handling code here:
        if(TNoObat.getText().isEmpty() || TNamaObat.getText().isEmpty() || TJenisObat.getText().isEmpty() || TDosis.getText().isEmpty() || THarga.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Ulangi, seluruh data harus diisi !");
                Mobat.HapusData();
                Mobat.empty_field();
                BTambah.setEnabled(false);
                Mobat.hitungData();
            }else{
                Mobat.SimpanData();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan","Insert Data",JOptionPane.INFORMATION_MESSAGE);
                Mobat.tabel();
                Mobat.empty_field();
                Mobat.hitungData();
                BTambah.setEnabled(true);
            }
        BTambah.setEnabled(true);
        BUbah.setEnabled(false);
        BHapus.setEnabled(false);
        BSimpan.setEnabled(false);
        TNoObat.requestFocus();
    }//GEN-LAST:event_BSimpanActionPerformed

