    private void BUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUbahActionPerformed
        // TODO add your handling code here:
        if(TNoObat.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Pilih Data Terlebih Dahulu !");
        }else if(TNoObat.getText().isEmpty() || TNamaObat.getText().isEmpty() || TJenisObat.getText().isEmpty() || TDosis.getText().isEmpty() || THarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ulangi, semua data harus diisi !");
        }else{
            Mobat.UbahData();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah","Update Data",JOptionPane.INFORMATION_MESSAGE);
            BTambah.setEnabled(true);
        }
        //Mdiagnosa.hitungData();
        TabelObat.requestFocus();
        Mobat.tabel();
        Mobat.empty_field();
        BUbah.setEnabled(false);;
        BHapus.setEnabled(false);
        BSimpan.setEnabled(false);
    }//GEN-LAST:event_BUbahActionPerformed

