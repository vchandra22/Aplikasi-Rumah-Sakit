    private void BHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHapusActionPerformed
        // TODO add your handling code here:
        if(TNoObat.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu !");            
        }else{
            int reply = JOptionPane.showConfirmDialog(this,"Hapus data ?","Peringatan", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (reply == JOptionPane.YES_OPTION){
                Mobat.HapusData();
                Mobat.tabel();
                Mobat.hitungData();
            }else{
                JOptionPane.showMessageDialog(this, "Batal menghapus data ini");
            }
        }
        Mobat.empty_field();                                    
    }//GEN-LAST:event_BHapusActionPerformed

